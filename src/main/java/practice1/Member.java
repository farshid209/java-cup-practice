package practice1;

public class Member {
    private Long id;
    private String name;
    private String nationalId;

    public Long getId() {
        return id;
    }

    public void setId(Long id1) {
        id = id1;
    }

    public String getName() {
        return name;
    }

    public void setName(String name1) {
        name = name1;
    }

    public String getNationalId() {
        return nationalId;
    }

    public void setNationalId(String nationalId1) {
        nationalId = nationalId1;
    }
}
